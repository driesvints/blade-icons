    public function boot(): void
    {
        $this->loadViewsFrom(__DIR__ . '/../resources/views', 'blade-icons');

        collect(self::HEROICONS)->each(function (string $icon) {
            Blade::component("blade-icons::components.heroicons.heroicon-o-$icon", "heroicon-o-$icon");
            Blade::component("blade-icons::components.heroicons.heroicon-s-$icon", "heroicon-s-$icon");
        });

        collect(self::ZONDICONS)->each(function (string $icon) {
            Blade::component("blade-icons::components.zondicons.zondicon-$icon", "zondicon-$icon");
        });

        collect(self::FEATHERICONS)->each(function (string $icon) {
            Blade::component("blade-icons::components.feathericons.feathericon-$icon", "feathericon-$icon");
        });

        if ($this->app->runningInConsole()) {
            $this->publishes([
                __DIR__ . '/../resources/images' => public_path('vendor/blade-icons'),
            ], 'blade-icons');
        }
    }
